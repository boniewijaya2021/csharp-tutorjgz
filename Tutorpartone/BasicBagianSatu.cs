using System;

namespace Tutorpartone
{
    class BasicBagianSatu
    {
        public static void Main(string[] args)
        {
            Console.WriteLine("=== Program Start ===");  // Log Start.
            Console.WriteLine(); // Mencetak baris kosong.
            Console.WriteLine("Hello, World!"); // Mencetak string.
            Console.WriteLine(42); // Mencetak integer.
            Console.WriteLine(3.14); // Mencetak angka desimal.
            Console.WriteLine("Hello, {0}!", "Amelia"); // Mencetak string yang diformat.
            Console.WriteLine("=== Program End ==="); // Log End.
        }
    }
}
